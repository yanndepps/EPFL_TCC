// easy on maths
function add(a, b) {
	return a + b;
}

function sub(a, b) {
	return a - b;
}

function mul(a, b) {
	return a * b;
}

console.log(add(5, 3));
console.log(sub(5, 3));
console.log(mul(5, 3));


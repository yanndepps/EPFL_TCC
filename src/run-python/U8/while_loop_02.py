# ---
# set an iterator to the value of 3.
# display "Hello" in the terminal as long as the
# iterator is smaller than 50
# ---

init_value = 3
max_value = 50
message = "Hello !"

while init_value < max_value:
    print(str(init_value) + " : " + message)
    init_value = init_value + 1

